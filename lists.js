// Parse the text into arrays
const parseLists = (text) => {
  const sections = text.split('\n\n');
  const lists = [];
  
  sections.forEach(section => {
    const lines = section.trim().split('\n');
    const items = [];
    
    lines.forEach(line => {
      // Skip section headers (lines in CAPS)
      if (line.match(/^[A-Z\s\(\)]+$/)) {
        return;
      }
      
      // Skip empty lines
      if (!line.trim()) {
        return;
      }
      
      // Add the line as an item (no numbers to remove)
      items.push(line.trim());
    });
    
    if (items.length > 0) {
      lists.push(items);
    }
  });
  
  return lists;
};

